#Aim: WAP to split a string using .split(). Try different seperators.
text = input("Enter a string(with space,comma,dash,colon): ")

print("\nSplit by space:")
print(text.split())

print("\nSplit by comma:")
print(text.split(","))

print("\nSplit by dash:")
print(text.split("-"))

print("\nSplit by colon:")
print(text.split(":"))

